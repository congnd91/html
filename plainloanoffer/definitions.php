                        <h1>Definitions</h1>
                        <p><strong>ACH</strong> - This stands for automatic clearing house, which is mainly a method used to transfer funds to and from bank accounts.</p>
                        <p><strong>APR or Annual Percentage Rate</strong> - This is the cost of credit which is expressed in a yearly rate. This is not the same as contract interest rate.</p>
                        <p><strong>Balance</strong> - This refers to the outstanding in your bank account.</p>
                        <p><strong>Bankruptcy</strong> - This is a legal proceeding in America's Federal Court which in entered into by a borrower. This is often someone who is not able to pay back his debts which allows for them to negotiate some form of
                            partial payment or the selling of a borrower's assets. Bankruptcy information will stay on the credit history of a person for up to a decade.</p>
                        <p><strong>Budget</strong> - A plan or method used for spending management and for saving money.</p>
                        <p><strong>Caps</strong> - The established limit of an amount's interest rate which can be increased to an adjustable rate mortgage loan.</p>
                        <p><strong>Cash advance</strong> - This refers to a source of cash which can be taken in the case of emergencies. This is for people who are employed but may not have access to other sources of credit. This is meant to bridge the
                            financial gap in between now and the next pay day. The interest is charged from the date it is advanced.</p>
                        <p><strong>Charge off</strong> - This is a credit card debt or loan which is written off as being uncollectible from a borrower. This at times is the case when the loan has been sold or given the debt to some collection agency. This
                            debt remains collectable.</p>
                        <p><strong>Checking account</strong> - The money which is kept in savings or a bank for safekeeping. This money can be withdrawn easily by simply writing out a check or using an ATM machine.</p>
                        <p><strong>Security or Collateral</strong> - Often an asset which is pledged in order to assure repayment of debt.</p>
                        <p><strong>Compound interest</strong> - Interest which is computed on the loan's balance, the balance will include all interest which is unpaid.</p>
                        <p><strong>Co-signer</strong> - An individual who willfully signs a loan agreement with the borrower and partly assumes responsibility of repayment of a loan.</p>
                        <p><strong>Credit</strong> - The promise to pay the amount at a later date for services or goods availed presently.</p>
                        <p><strong>Credit request</strong> - A request for credit given in writing. At times an request fee will be charged in order to cover the cost of processing the loan.</p>
                        <p><strong>Credit bureau</strong> - An organization that works to compile the credit histories of would be borrowers and also provides these reports to lenders. These reports are used by lenders for making decisions. Experian, TransUnion
                            and Equifax are the largest credit reporting agencies in America.</p>
                        <p><strong>Credit card</strong> - This is a card issued by the bank in order to make payments for purchases. The outstanding balance is subject to interest.</p>
                        <p><strong>Credit counseling</strong> - A type of counseling which is provided by organizations aimed at helping consumers with ways to repair their credit so as to get their financial affairs back on track.</p>
                        <p><strong>Credit limit</strong> - The most amount of money which is allowed to be charged on a line of credit or credit card.</p>
                        <p><strong>Credit line</strong> - This is also often referred to personal line of credit and is the maximum amount a person can get against his or her account. Once the credit line has been repaid the person can then re-borrow against
                            this account.</p>
                        <p><strong>Credit report </strong>- This is a report which is actually the history of a person's debt repayment, outstanding debts, bankruptcies and late payments. It will also have his or her bankruptcies.
                        </p>
                        <p><strong>Creditor</strong> - A business or person from who you are borrowing or someone you owe money to.</p>
                        <p><strong>Debit card</strong> - This is a card issued by a bank or some other financial institution and often used for purchases. The purchase is deducted directly from a checking account.</p>
                        <p><strong>Debt</strong> - The amount which is owed to a lender.</p>
                        <p><strong>Debt Consolidation</strong> - This refers to a strategy which is at times used by people to improve their debt management issues. Instead of opting to pay several bills every month a consumer will just pay his debt with
                            one bill to one financial institution.</p>
                        <p><strong>Default</strong> - The failure to pay back a loan or meet the terms the loan agreement.
                        </p>
                        <p><strong>Delinquency</strong> - Failing to pay on time.</p>
                        <p><strong>Direct Deposit </strong>- This is an electronic funds transfer directly to a bank account, so paper check is not needed.</p>
                        <p><strong>Equal Credit Opportunity Act</strong> - This is a federal law which prohibits lenders from any sort of applicant discrimination.</p>
                        <p><strong>E-Signature</strong> - Often referred to as an electronic signature this requires a software which binds your signature or some other mark to a document. The E-sign bill was passed by the government in June 2000 which legalizes
                            this signature.</p>
                        <p><strong>The Fair Credit Reporting Act</strong> - This is a federal law which gives borrowers the right to lean exactly what info credit reporting agencies currently have on them, it also enables them to dispute incorrect data.</p>
                        <p><strong>Fair Debt Collections Practices Act</strong> - This is a federal level law that works to protect people from abusive or harassing conduct or misleading and false representations for debt collection.</p>
                        <p><strong>FDIC or Federal Deposit Insurance Corporation</strong> - Usually a federal agency which insures a consumer's deposit in their savings and for a loan of up to $100,000 for every account. These deposits will include savings
                            and checking accounts and also deposit certificates.
                        </p>
                        <p><strong>Finance charge</strong> - Credit costs expressed in a dollar amount.</p>
                        <p><strong>A fixed interest rate</strong> - A rate of interest which will not change through the term of the loan.</p>
                        <p><strong>Foreclosure</strong> - This is a legal process in which collateral that has been pledged for a loan can be sold in order to repay the loan if the borrower defaults.</p>
                        <p><strong>Installment loan</strong> - This is a loan that has a predetermined number of payments and loan amount.</p>
                        <p><strong>Interest</strong> - A fee that the lender charges for borrowing a sum of money.</p>
                        <p><strong>Interest rate </strong>- A rate that a lender will charge borrowers in order to borrow money from them. It is expressed in percentage % per annum.</p>
                        <p><strong>Judgment</strong> - This is a court order which is made by the court and which is related to a lawsuit. It actually decides who wins the case.</p>
                        <p><strong>Late payment fee</strong> - This is a charge for a payment which is not received in time.
                        </p>
                        <p><strong>Lease</strong> - This is a legal contract which allows a consumer to use some asset like a car for payment. The asset needs to be returned once the lease term ends.</p>
                        <p><strong>Lender</strong> - A business or a person who lends or even offers loans to people.</p>
                        <p><strong>Liable</strong> - to have legal responsibility.</p>
                        <p><strong>Lien</strong> - When a creditor lays claim to a piece of property to ensure his debt is paid off.</p>
                        <p><strong>Loan</strong> - Any amount which is borrowed so that it can be repaid later with added interest.
                        </p>
                        <p><strong>Loan Agreement</strong> - This is a legal contract which details the conditions and the terms of the loan.</p>
                        <p><strong>Mortgage loan </strong>- This is used to purchase a piece of real estate. Here the property is actually the security for this loan.</p>
                        <p><strong>Public Record</strong> - This is information which is gotten from federal, state or other sources which details a person's history of financial obligations which includes child support and alimony.</p>
                        <p><strong>Refinance</strong> - To pay off an existing loan with whatever proceeds have been had from the new loan in order to enjoy a lower interest rate.</p>
                        <p><strong>Repossess</strong> - The voluntary or forced surrender of items in the case of a consumer's failure to pay back a loan.</p>
                        <p><strong>Right of recession</strong> - The right of a borrower to cancel the contract within just three working days.</p>
                        <p><strong>Savings account</strong> - The money which his kept in a savings account and for safekeeping. Big reason here is that savings accounts will earn interest on money kept in this account.
                        </p>
                        <p><strong>Secured loan</strong> - This is a loan where the borrower will pledge his asset like a car or home which will be sold if he is not able to pay back the loan.</p>
                        <p><strong>Security</strong> - check collateral.</p>
                        <p><strong>Simple interest</strong> - The interest is computed on the basis of outstanding as long as some portions remain still unpaid.</p>
                        <p><strong>Title</strong> - A document which proves ownership of property.</p>
                        <p><strong>Truth in Lending Act</strong> - This is a federal law which mostly requires lenders to be able to disclose to their borrowers the actual cost of the loan. This will include the actual interest rate as well as the terms
                            and conditions of this loan in an easy to understand fashion.
                        </p>
                        <p><strong>Unsecured loan </strong>- This is a loan given on the bases of a borrower's word to pay back.
                        </p>
                        <p><strong>Variable interest rate</strong> - This is an interest rate which will change based on the current index, like a prime rate.</p>
                        <p><strong>Yield</strong> - Often used for an effective rate of return which is paid on money market accounts, bonds or savings.</p>
